package Numberprogramming;

public class Swaping_Two_Value {
public static void main(String[] args) {
	int a=10,b=20;
	
	System.out.println("before swaping ");
	System.out.println("a value : "+a+" ,  b value : "+b);
	
	int temp=a;
	a=b;
	b=temp;
	System.out.println("after swaping ");
	System.out.println("a value : "+a+" ,  b value : "+b);
	
}
}
